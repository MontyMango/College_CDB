package Class_Exercises;

public class ClassExercise3 {
    public static void main(String[] args) {
        double CountryA = 50000000, CountryB = 70000000;
        int Years = 0;
        
        while (CountryA < CountryB) {
            
            CountryA = (CountryA * 0.03) + CountryA;
            CountryB = (CountryB * 0.02) + CountryB;
            
            System.out.println(CountryA + " " + CountryB);
            
            ++Years;
        }
        System.out.println("It will take " + Years + " years for Country A to pass Country B");
    }
}
